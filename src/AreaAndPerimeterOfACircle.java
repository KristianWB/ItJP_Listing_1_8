public class AreaAndPerimeterOfACircle {
    public static void main(String[] args)   {

        /*
         we need to built two print commands that displays the area and the perimeter of a circle with a predetermined radius
          */
            // First the perimeter
        System.out.println(String.format("%.2f", (2 * 6.5 * Math.PI)));

            // Second the area
        System.out.println(String.format("%.2f",(6.5 * 6.5 * Math.PI)));
    }
}
